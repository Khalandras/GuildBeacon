function GB.ApplyLocale(locale)
    GB.Locale = GB.LocaleCatalogs[locale] or GB.LocaleCatalogs.enUS
end

function GB.L(key)
    return (GB.Locale or GB.LocaleCatalogs.enUS)[key] or key
end

